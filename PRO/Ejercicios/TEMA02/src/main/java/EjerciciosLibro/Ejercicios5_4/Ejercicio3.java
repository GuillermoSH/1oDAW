/**
 * Muestra los numeros multiplos de 5 de 0 a 100 utilizando un bucle while.
 */
package EjerciciosLibro.Ejercicios5_4;

public class Ejercicio3 {
    public static void main(String args[]){
        int i=0;

        while(i<=100){
            System.out.println(i);
            i+=5;
        }
    }
}
