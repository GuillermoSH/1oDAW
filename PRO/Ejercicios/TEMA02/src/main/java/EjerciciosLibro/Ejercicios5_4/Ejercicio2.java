/**
 * Muestra los números múltiplos de 5 de 0 a 100 utilizando un bucle do-while.
 */
package EjerciciosLibro.Ejercicios5_4;

public class Ejercicio2 {
    public static void main(String args[]){
        int i=0;

        do{
            System.out.println(i);
            i+=5;
        } while(i<=100);
            System.exit(1);
    }
}
