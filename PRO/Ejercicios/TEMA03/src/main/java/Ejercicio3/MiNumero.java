package Ejercicio3;

public class MiNumero {
    private int numero;

    public MiNumero(int numero){
        this.numero=numero;
    }

    public int doble(){
        return this.numero*2;
    }
    public int triple(){
        return this.numero*3;
    }
    public int cuadruple(){
        return this.numero*4;
    }
}
